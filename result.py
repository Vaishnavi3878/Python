p=float(input("Enter percentage:"))
if(p>=90):
    print("Grade:O")
elif(p>=85):
    print("Grade:A+")
elif(p>=80):
    print("Grade:A")
elif(p>=75):
    print("Grade:B+")
elif(p>=70):
    print("Grade:B")
elif(p>=65):
    print("Grade:C+")
elif(p>=60):
    print("Grade:C")
elif(p>=55):
    print("Grade:D+")
elif(p>=50):
    print("Grade:D")
elif(p>=40):
    print("Grade:PASS")
else:
    print("Grade:FAIL")




