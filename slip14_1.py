from tkinter import *
def surface_Area():
    t3.delete(0,"end")
    r=float(t1.get())
    h=float(t2.get())
    a=(2*3.14*r*h)+(2*3.14*r*r)
    a=float(a)
    t3.insert(0,a)
def volume():
    t3.delete(0,"end")
    r=float(t1.get())
    h=float(t2.get())
    v=3.14*r*r*h
    v=float(v)
    t3.insert(0,v)
window=Tk()
window.geometry("500x600")
l1=Label(window,text="Enter the redius of cylinder:")
l2=Label(window,text="Enter the height of cylinder:")
l3=Label(window,text="Result:")
t1=Entry(window,width=30)
t2=Entry(window,width=30)
t3=Entry(window,width=30)
b1=Button(window,text="Surface area",command=surface_Area)
b2=Button(window,text="Volume",command=volume)
l1.place(x=50,y=50)
l2.place(x=50,y=100)
l3.place(x=50,y=150)
t1.place(x=250,y=50)
t2.place(x=250,y=100)
t3.place(x=250,y=150)
b1.place(x=50,y=200)
b2.place(x=250,y=200)
window.mainloop()
